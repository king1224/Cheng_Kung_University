day=input('Please enter the day of a week:','s')
switch(day)
    case 'Monday',
        disp('2');
    case 'Tuesday',
        disp('3');
    case 'Wednesday',
        disp('4');
    case 'Thurday',
        disp('5');
    case 'Friday',
        disp('6');
    case 'Saturday',
        disp('7');
    case 'Sunday',
        disp('1');
    otherwise,
        disp('No number can be presented');
end